async function handleLogin() {
    const usernameEl = document.getElementById('login-username');
    const passwordEl = document.getElementById('login-password');
    const loginBtn = document.getElementById('login-submit-btn');
    const loginError = document.getElementById('login-error');
    const accessError = document.getElementById('access-error');

    const username = usernameEl.value.trim();
    const password = passwordEl.value;

    // Validate inputs before sending
    if (!username || !password) {
        loginError.textContent = 'Please enter both username and password.';
        loginError.classList.remove('hidden');
        accessError.classList.add('hidden');
        return;
    }

    // Reset error messages
    loginError.classList.add('hidden');
    accessError.classList.add('hidden');

    // Show loading state on button
    loginBtn.disabled = true;
    loginBtn.innerHTML = '<i data-lucide="loader-2" class="spin"></i> Logging in...';
    lucide.createIcons();

    console.log(`[Login] Attempting login for user: "${username}"`);

    try {
        const response = await fetch('/api/auth/login', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ username, password })
        });

        console.log(`[Login] Server responded with status: ${response.status}`);

        if (response.status === 401) {
            // Invalid credentials
            console.warn('[Login] Invalid credentials.');
            loginError.textContent = 'Incorrect username or password.';
            loginError.classList.remove('hidden');
            passwordEl.value = '';
            passwordEl.focus();
            return;
        }

        if (!response.ok) {
            // Other server errors (500, 503 etc.)
            const detail = await response.text();
            console.error(`[Login] Server error ${response.status}:`, detail);
            loginError.textContent = `Server error (${response.status}). Please try again later.`;
            loginError.classList.remove('hidden');
            return;
        }

        const data = await response.json();
        console.log('[Login] Login successful. User data received:', data.user);

        // Store JWT token
        accessToken = data.access_token || null;
        if (accessToken) setToken(accessToken);

        const user = {
            id: data.user.id,
            username: data.user.username,
            role: data.user.role || ROLE_REPORTS_ONLY,
            isActive: data.user.isActive !== false,
        };

        // Check permissions based on what the user intends to do
        if (loginIntent === 'manifest') {
            if (user.role === ROLE_ADMIN || user.role === ROLE_DISPATCH) {
                console.log('[Login] Manifest access granted (role=' + user.role + ').');
                finalizeLogin(user);
            } else {
                console.warn('[Login] User lacks manifest permission (role=' + user.role + ').');
                accessError.textContent = 'You do not have permission to access the Manifest system. Reports Only access.';
                accessError.classList.remove('hidden');
                loginError.classList.add('hidden');
            }
        } else {
            // Report intent - all authenticated users can view reports
            console.log('[Login] Report access granted.');
            finalizeLogin(user);
        }

    } catch (error) {
        // Network-level failure (no connection, DNS failure, CORS etc.)
        console.error('[Login] Network error during login:', error);
        loginError.textContent = 'Could not connect to the server. Check your network connection.';
        loginError.classList.remove('hidden');
    } finally {
        loginBtn.disabled = false;
        loginBtn.textContent = 'Login';
    }
}

async function finalizeLogin(user) {
    currentUser = user.username;
    currentUserId = user.id;
    currentUserRole = user.role || ROLE_REPORTS_ONLY;
    setCurrentUser(user.username);
    setUserRole(user.role);


    // Run the full authenticated dashboard init (skipped at startup because
    // the user was unauthenticated when DOMContentLoaded fired).
    await loadSettings();
    initTruckDropdown();
    initPersonnelDropdowns();
    await loadState();
    initManifestNumber();
    setDefaultDate();
    renderTable();
    if (formInputs.regNumber.value) {
        handleTruckChange();
    }

    // If manifest number is missing, set placeholder (backend assigns on save)
    if (!formInputs.manifestNumber.value) {
        formInputs.manifestNumber.value = MANIFEST_PLACEHOLDER;
    }


    // Hide Modal & Landing
    document.getElementById('login-modal').classList.remove('visible');
    document.getElementById('login-modal').classList.add('hidden');
    hideLandingPage();

    // Apply UI Permissions
    applyPermissions();

    // Navigate if needed
    if (loginIntent === 'report') {
        showReports();
    }
}

function applyPermissions() {
    if (!currentUser) return;

    console.log("Applying permissions - User:", currentUser, "Role:", currentUserRole);

    const formSections = document.querySelectorAll('.form-section');
    const previewSection = document.querySelector('.preview-section');
    const settingsBtn = document.getElementById('settings-btn');
    const resetBtn = document.getElementById('reset-btn');

    const isAdmin    = currentUserRole === ROLE_ADMIN;
    const isDispatch = currentUserRole === ROLE_DISPATCH;
    const canManifest = isAdmin || isDispatch;

    // Manifest UI — visible to ADMIN and DISPATCH only; hidden for REPORTS_ONLY
    formSections.forEach(el => {
        if (canManifest) el.classList.remove('guest-hidden');
        else             el.classList.add('guest-hidden');
    });
    if (previewSection) {
        if (canManifest) previewSection.classList.remove('guest-hidden');
        else             previewSection.classList.add('guest-hidden');
    }
    if (resetBtn) {
        if (canManifest) resetBtn.classList.remove('guest-hidden');
        else             resetBtn.classList.add('guest-hidden');
    }

    // Settings & Users tab — ADMIN only
    if (isAdmin) {
        if (settingsBtn) settingsBtn.classList.remove('guest-hidden');
    } else {
        if (settingsBtn) settingsBtn.classList.add('guest-hidden');
    }

    // Analytics — office roles only; hidden from DRIVER
    const analyticsBtn = document.getElementById('analytics-btn');
    if (analyticsBtn) {
        if (currentUserRole === 'DRIVER') analyticsBtn.classList.add('guest-hidden');
        else analyticsBtn.classList.remove('guest-hidden');
    }
}


// Logout Function
function handleLogout() {
    currentUser = null;
    currentUserId = null;
    currentUserRole = null;
    accessToken = null;
    loginIntent = null;
    clearToken();

    // Hide any open modals
    document.querySelectorAll('.modal').forEach(m => {
        m.classList.add('hidden');
        m.classList.remove('visible');
    });

    // Show landing page
    showLandingPage();
}

