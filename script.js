// Excel Generation — pure file-building function.
// manifestNum must be the backend-issued number; never call this with a placeholder.
async function generateExcel(manifestNum) {
    const workbook = new ExcelJS.Workbook();
    const sheet = workbook.addWorksheet('Manifest');

    // Set column widths
    sheet.columns = [
        { width: 15 }, // A: Invoice
        { width: 30 }, // B: Customer Name
        { width: 15 }, // C: Customer Number
        { width: 15 }, // D: Area
        { width: 8 },  // E: SKU
        { width: 10 }, // F: Value
        { width: 10 }, // G: Weight
        { width: 10 }, // H: Date
        { width: 10 }, // I: Time In
        { width: 10 }, // J: Time Out
        { width: 15 }, // K: Sign
    ];

    // Row 8: Table Header
    const headerRow = sheet.getRow(8);
    headerRow.values = ['INVOICE', 'CUSTOMER NAME', 'ACCOUNT', 'AREA', 'SKU', 'VALUE', 'WEIGHT KG', 'DATE', 'TIME IN', 'TIME OUT', 'CUSTOMER SIGN'];
    headerRow.eachCell((cell) => {
        cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF4472C4' } };
        cell.font = { color: { argb: 'FFFFFFFF' }, bold: true };
        cell.alignment = { horizontal: 'center' };
        cell.border = { top: { style: 'thin' }, left: { style: 'thin' }, bottom: { style: 'thin' }, right: { style: 'thin' } };
    });

    // Data Rows
    // VALUE is written as "CUR 1,234.56" text (not a numeric $-formatted cell) —
    // a mixed USD/ZWL manifest must never look like a single-currency figure.
    orders.forEach(order => {
        const row = sheet.addRow([
            order.invoice,
            order.customer,
            order.customerNumber || 'N/A',
            order.area,
            order.sku,
            formatCurrencyValue(order.value, order.currency),
            order.weight,
            order.invoiceDate ? formatDate(order.invoiceDate) : '',
            '', '', ''
        ]);
        row.eachCell((cell) => {
            cell.border = { top: { style: 'thin' }, left: { style: 'thin' }, bottom: { style: 'thin' }, right: { style: 'thin' } };
        });
    });

    // Totals Row — one line per currency in the VALUE cell, never blended.
    // Column indices match the header row (E=SKU, F=Value, G=Weight); a
    // pre-existing off-by-one here previously put the SKU total under Area
    // and the value/weight totals one column left of their headers, which
    // would have put this currency breakdown under the wrong column too.
    const lastRow = sheet.lastRow.number + 1;
    const totalRow = sheet.getRow(lastRow);
    const excelTotals = calcTotals(orders);
    const excelValueTotalsByCurrency = calcValueTotalsByCurrency(orders);
    totalRow.getCell(1).value = 'Grand Total';
    totalRow.getCell(5).value = excelTotals.sku;
    totalRow.getCell(6).value = excelValueTotalsByCurrency
        .map(t => formatCurrencyValue(t.value, t.currency))
        .join('\n');
    totalRow.getCell(6).alignment = { wrapText: true };
    totalRow.getCell(7).value = excelTotals.weight;
    totalRow.eachCell((cell) => {
        cell.font = { bold: true };
        cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FFD9D9D9' } };
        cell.border = { top: { style: 'thin' }, left: { style: 'thin' }, bottom: { style: 'thin' }, right: { style: 'thin' } };
    });

    const buffer = await workbook.xlsx.writeBuffer();
    const fileName = `Manifest_${manifestNum}.xlsx`;

    saveBlob(buffer, fileName);

    const blob = new Blob([buffer], { type: 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet' });
    uploadManifest(blob, fileName);
}

function saveBlob(buffer, fileName) {
    const blob = new Blob([buffer], { type: 'application/octet-stream' });
    const link = document.createElement('a');
    link.href = URL.createObjectURL(blob);
    link.download = fileName;
    link.click();
}

async function uploadManifest(blob, filename) {
    const formData = new FormData();
    formData.append('file', blob, filename);

    try {
        const response = await apiFetch(`${API_URL}/manifests/save`, {
            method: 'POST',
            body: formData
        });

        if (!response.ok) {
            console.warn('Failed to save manifest to server');
        } else {
            console.log('Manifest saved to server');
        }
    } catch (error) {
        console.error('Error uploading manifest:', error);
    }
}

function formatDate(dateStr) {
    if (!dateStr) return '';
    const d = new Date(dateStr);
    const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
    return `${d.getDate()}-${months[d.getMonth()]}-${d.getFullYear().toString().substr(-2)}`;
}

