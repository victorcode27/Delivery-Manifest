// Logic
async function addOrder() {
    const invoiceNum = orderInputs.invoice.value.trim();
    const customer   = orderInputs.customer.value.trim();
    const orderNum   = orderInputs.orderNumber.value.trim() || '';
    const area       = orderInputs.area.value.trim() || 'UNKNOWN';
    const value      = parseFloat(orderInputs.value.value) || 0;

    if (!invoiceNum || !customer) {
        alert('Please fill in at least Invoice Number and Customer Name.');
        return;
    }

    const btn = document.getElementById('add-order-btn');
    btn.disabled = true;

    try {
        // Single atomic call: create the invoice and stage it in one transaction
        const resp = await apiFetch(`${API_URL}/invoices/manual-and-stage`, {
            method: 'POST',
            body: JSON.stringify({
                invoice_number:  invoiceNum,
                order_number:    orderNum,
                customer_name:   customer,
                total_value:     String(value),
                area:            area,
                customer_number: 'N/A'
            })
        });

        if (!resp.ok) {
            const err = await resp.json().catch(() => ({}));
            alert(`Failed to add invoice: ${err.detail || resp.statusText}`);
            return;
        }

        // Refresh the manifest table from API, then clear the form
        await renderTable();
        clearOrderInputs();

    } catch (err) {
        console.error('Error adding order:', err);
        alert('An unexpected error occurred. Please try again.');
    } finally {
        btn.disabled = false;
    }
}

async function removeOrder(id) {
    if (confirm('Remove this order?')) {
        // Find the order to get its filename
        const order = orders.find(o => o.id === id);
        if (!order) return;

        // Call API to remove from staging
        try {
            const response = await apiFetch(`${API_URL}/manifest/remove`, {
                method: 'POST',
                body: JSON.stringify({ filenames: [order.filename || order.invoice] })
            });

            if (!response.ok) {
                console.warn('Could not remove invoice from staging');
            }
        } catch (error) {
            console.error('Error removing from staging:', error);
        }

        // Reload manifest from API to refresh the list
        await renderTable();
    }
}

